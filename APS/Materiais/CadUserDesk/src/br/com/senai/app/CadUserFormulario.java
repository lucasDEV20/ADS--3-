    private void ConsultaUser() {

        modelo.setNumRows(0);
        List<User> listaUser = new ArrayList<User>();

        listaUser = user.getConsulta();

        if (listaUser.size() > 0) {

            for (int i = 0; i < listaUser.size(); i++) {
                modelo.addRow(new Object[]{listaUser.get(i).getUserid(),
                    listaUser.get(i).getFirstName(),
                    listaUser.get(i).getLastName(),
                    listaUser.get(i).getDob(),
                    listaUser.get(i).getEmail()});
            }

        } else {
            modelo.setNumRows(0);
        }

    }

