    private void btnSalvarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSalvarActionPerformed
        // TODO add your handling code here:
        try {

            usuario.setFirstName(txtNome.getText());
            usuario.setLastName(txtSobrenome.getText());
            usuario.setDob(createNewDate(txtDataNascimento.getText()));
            usuario.setEmail(txtEmail.getText());

            if (btnSalvar.getLabel().equals("Salvar")) {
                user.Adicionar(usuario);
            } else {
                user.Alterar(usuario);
            }
            ConsultaUser();
            LimparCampos();
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, e.getMessage(), "AtenÃ§Ã£o!!!", JOptionPane.INFORMATION_MESSAGE);
        }
    }//GEN-LAST:event_btnSalvarActionPerformed

    private void btnExcluirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnExcluirActionPerformed
        try {
            // TODO add your handling code here:
            user.Remover(user.getConsultaPorId(usuario.getUserid()));
        } catch (Exception ex) {
            Logger.getLogger(CadUserFormulario.class.getName()).log(Level.SEVERE, null, ex);
        }
        ConsultaUser();
        LimparCampos();
    }//GEN-LAST:event_btnExcluirActionPerformed

    private void tlbUserMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tlbUserMouseClicked
        // TODO add your handling code here:
        int linha = tlbUser.getSelectedRow();
        Integer codigo = (Integer) tlbUser.getValueAt(linha, 0);
        PreencheCampos((int) codigo);
    }//GEN-LAST:event_tlbUserMouseClicked

    private void btnNovoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnNovoActionPerformed
        // TODO add your handling code here:
        LimparCampos();
    }//GEN-LAST:event_btnNovoActionPerformed

    private void LimparCampos() {
        usuario = new User();

        txtNome.setText("");
        txtSobrenome.setText("");
        txtDataNascimento.setText(new String());
        txtEmail.setText("");

        btnSalvar.setLabel("Salvar");
    }

    private void PreencheCampos(int Iden) {
        if (Iden > 0) {
            usuario = user.getConsultaPorId(Iden);
            txtNome.setText(usuario.getFirstName());
            txtSobrenome.setText(usuario.getLastName());
            txtDataNascimento.setText(convertDate(usuario.getDob()));
            txtEmail.setText(usuario.getEmail());
            btnSalvar.setLabel("Editar");
        } else {
            btnSalvar.setLabel("Salvar");
        }
    }

